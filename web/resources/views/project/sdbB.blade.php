	<section class="sec-type-1 wrap-narrow">
		<h4>공세권, 앞으로 더 중요합니다</h4>
		<p>신대방B점에서 가장 가까운 공원인 보라매공원은 명실상부 서남권의 대표 공원입니다. 봄, 가을철에는 백일장 및 사생대회를 진행하는 등 지역 공원으로서 역할을 제대로 하고 있습니다. 또한 봄에는 벚꽃 명소로서 벚꽃이 가득 만개한 아름다운 경관을 선사합니다.<br><br>또한 관악구민들의 휴식을 책임지는 ‘도림천’은 총 11km에 걸쳐 자전거 도로와 산책로가 조성되어 수변 공원의 역할을 하고 있습니다. 간단한 산책이나 라이딩 등 휴식을 즐기기에 좋습니다.</p>
