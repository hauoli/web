	<section class="sec-type-1 wrap-narrow">
		<h4>삶의 질을 높여주는 주변 환경</h4>
		<p>신대방B점은 1분 거리에 슈퍼마켓과 편의점등이 있어 생필품을 구입하는 데 아주 편리합니다. 또한 도보 이동이 가능한 가까운 거리에 롯데백화점이 있어, 쇼핑을 즐기기에도 더할 나위 없이 좋습니다.<br><br>놀거리와 먹거리가 가득한 신림사거리는 신대방B점에서 약 1.3km 떨어져 있습니다. 신림사거리의 시끌벅적한 분위기는 일상의 고독함을 잠시 내려놓을 수 있도록 즐길 거리를 제공하고 있습니다.</p>
	</section>
