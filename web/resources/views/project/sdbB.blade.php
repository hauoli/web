	<section class="sec-type-1 wrap-narrow">
		<h4>안전, 또 안전</h4>
		<p>주거공간은 안전하고 안락해야 합니다. 때문에 하우올리는 건물의 보안에 관련된 것이라면 그 어떤 요소도 쉽게 생각하지 않았습니다. 하우올리 신대방B점의 보안시스템은 24시간, 공용공간과 외부를 감시하는 CCTV는 물론, 신원확인이 가능한 세대 인터폰을 설치해 거주자분들께서 1년 365일 언제나 편안함과 안정감을 느낄 수 있도록 했습니다.</p>
