		<p>신대방B의 첫인상은 따뜻하고 모던한 느낌의 올화이트 석재타일로 이루어져 있습니다. 이 석재타일은 ‘인조 라임스톤’이라는 자재입니다. 부드러운 표면, 질감, 색감이 특징인 석자재로서 따뜻하고 모던한 느낌을 살리는데 탁월한 역할을 하고 있습니다. 이러한 외장재를 사용한 하우올리 신대방B점은 주변 건물들 사이에서 깨끗하면서도 따뜻하고 화사한 느낌이 부각됩니다.</p>
	</section>
