	<section class="sec-type-1 wrap-narrow">
		<h4>1인 가구의 성지, 관악구에 주목하다</h4>
		<p>관악구는 1인가구의 성지입니다. 2호선의 서쪽에는 GBD(구로ㆍ가산 권역)가 있고, 동쪽으로도 GBD(강남 권역)가 있어 해당 권역으로 출퇴근하는 1인가구 직장인들에게 이동의 편리함이 보장된다는 장점이 있습니다.</p>
